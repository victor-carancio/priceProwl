export const formatToDecimals = (price: number, decimals: number) => {
  return (price / Math.pow(10, decimals)).toFixed();
};

export const calculateDiscountPercent = (
  originalPrice: number,
  discount: number,
) => {
  return (100 - ((originalPrice - discount) / originalPrice) * 100).toFixed();
};
